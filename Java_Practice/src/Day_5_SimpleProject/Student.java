package Day_5_SimpleProject;

public class Student {
    private String name;
    private String id;
    private String sex;
    private int age;
    private String dept;
    private String year;

    public Student (String n, String id, String sex, int age, String dept, String yr ){
        name = n;
        this.id = id;
        this.sex = sex;
        this.age = age;
        this.dept = dept;
        year = yr;
    }
    
}
